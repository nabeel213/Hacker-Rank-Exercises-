import java.util.*;
public class Loops2
{   
    public static void main(String [] args)
    {      
        int a = 0;
        int b = 2;
        int n = 10;    
       
              
        for (int j = 0; j < n; j++) { 
                
            a = a + b;
            
            System.out.print(a + " "); 
            
            
            b = b * 2;
    
        } 
              
        System.out.println();
                
    }
}
